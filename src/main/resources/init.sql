CREATE TABLE
    SYS_CUBE_CONFIG
    (
        PROJECT VARCHAR2(40) NOT NULL,
        CUBE_ID VARCHAR2(30) NOT NULL,
        CUBE_CONFIG VARCHAR2(4000),
        CUBE_TYPE VARCHAR2(30) NOT NULL,
