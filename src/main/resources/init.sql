    'cube配置表';
COMMENT ON COLUMN SYS_CUBE_CONFIG.CUBE_CONFIG
IS
    'cube的度量和维度信息，当type为table/chart时会有';
COMMENT ON COLUMN SYS_CUBE_CONFIG.CUBE_TYPE
IS
    'cube类型，char/table/text/temp';